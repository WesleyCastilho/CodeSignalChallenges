import unittest

from arrays.sudoku.sudoku import Sudoku2


class TestSudoku(unittest.TestCase):
    def test_sudoku(self):
        grid = [[".", ".", ".", "1", "4", ".", ".", "2", "."],
                [".", ".", "6", ".", ".", ".", ".", ".", "."],
                [".", ".", ".", ".", ".", ".", ".", ".", "."],
                [".", ".", "1", ".", ".", ".", ".", ".", "."],
                [".", "6", "7", ".", ".", ".", ".", ".", "9"],
                [".", ".", ".", ".", ".", ".", "8", "1", "."],
                [".", "3", ".", ".", ".", ".", ".", ".", "6"],
                [".", ".", ".", ".", ".", "7", ".", ".", "."],
                [".", ".", ".", "5", ".", ".", ".", "7", "."]]
        is_sudoku = Sudoku2.sudoku(grid)
        self.assertTrue(is_sudoku)
